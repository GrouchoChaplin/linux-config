#!/bin/bash 

#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
pathadd() 
{
    if [ -d "$1" ] && [[ ":$PATH:" != *":$1:"* ]]; then
        PATH="${PATH:+"$PATH:"}$1"
    fi
}

#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
libpathadd() 
{
    if [ -d "$1" ] && [[ ":$LD_LIBRARY_PATH:" != *":$1:"* ]]; then
        LD_LIBRARY_PATH="${LD_LIBRARY_PATH:+"$LD_LIBRARY_PATH:"}$1"
    fi
}


#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
function pathremove () 
{
    local IFS=':'
    local NEWPATH
    local DIR
    local PATHVARIABLE=${2:-PATH}

	for DIR in ${!PATHVARIABLE} ; do
		if [ "$DIR" != "$1" ] ; then
			NEWPATH=${NEWPATH:+$NEWPATH:}$DIR
		fi
    done
    export $PATHVARIABLE="$NEWPATH"
}

#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
function pathprepend () 
{
	pathremove $1 $2
	local PATHVARIABLE=${2:-PATH}
	export $PATHVARIABLE="$1${!PATHVARIABLE:+:${!PATHVARIABLE}}"
}

#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
function pathappend () 
{
	pathremove $1 $2
	local PATHVARIABLE=${2:-PATH}
	echo $2
	export $PATHVARIABLE="${!PATHVARIABLE:+${!PATHVARIABLE}:}$1"
}

#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
function path() 
{
	echo -e ${PATH//:/\\n}
}

#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
function libpath() 
{
	echo -e ${LD_LIBRARY_PATH//:/\\n}
}

#-------------------------------------------------------------------------------
#
#-------------------------------------------------------------------------------
function filepath() 
{
	echo -e ${OSG_FILE_PATH//:/\\n}
}

#-------------------------------------------------------------
# parse PATH, LD_LIBRARY_PATH and OSG_FILE_PATH and print
#-------------------------------------------------------------
function paths()
{
	printf "*******************************************************************\n"
	printf "*   PATH PATH PATH PATH PATH PATH PATH PATH PATH PATH PATH PATH   *\n"
	printf "*******************************************************************\n"
	path

	printf "*******************************************************************\n"
	printf "* LD_LIBRARY_PATH LD_LIBRARY_PATH LD_LIBRARY_PATH LD_LIBRARY_PATH *\n"
	printf "*******************************************************************\n"
	libpath

	printf "*******************************************************************\n"
	printf "*     OSG_FILE_PATH OSG_FILE_PATH OSG_FILE_PATH OSG_FILE_PATH     *\n"
	printf "*******************************************************************\n"
	filepath

}
