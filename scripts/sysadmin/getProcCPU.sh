function usage () 
{
	printf "\n"
	printf "No process specified.  You must specify at least ONE process\n"       
	printf "\n"
	printf "   Usage:\n" 
	printf "\n"
	printf "     $0 processName \n"
	printf "\n"
	exit
}

#---------------------------------------------------------------------
#
#---------------------------------------------------------------------

procName=$1

if [[ -z "${procName// }" ]]; then
	usage
	echo ""
else
	processID=$(pgrep -f $procName)
 	printf "\nProcess Name: %s \n" "${procName}"
	taskset -c -p "${processID}"
	printf "\n"
fi      
